package day13;

public class TestStringStartWithOrEndWith {
    public static void main(String[] args) {
        String name = "Esra";


        // chech if a String start with another String
        System.out.println(name.startsWith("Es"));
        System.out.println(name.startsWith("es"));
        System.out.println(name.startsWith("Ear"));

        // check if a String end with another String
        System.out.println(name.endsWith("a"));
        System.out.println(name.endsWith("E"));
        System.out.println(name.endsWith("A"));












    }

}
